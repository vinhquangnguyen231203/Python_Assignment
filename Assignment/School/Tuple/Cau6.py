# Danh sách các số
numbers = (10, 20, 30, 40, 50)

# Số lớn nhất
max_number = max(numbers)

# Số nhỏ nhất
min_number = min(numbers)

# Số trung bình
average_number = sum(numbers) / len(numbers)

# Tạo tuple chứa số lớn nhất, nhỏ nhất và số trung bình
result_tuple = (max_number, min_number, average_number)

# In ra tuple kết quả
print("Tuple chứa số lớn nhất, nhỏ nhất và số trung bình:", result_tuple)
