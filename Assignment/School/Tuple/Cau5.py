# Tạo một tuple với 10 phần tử bất kỳ
my_tuple = (10, 'hello', 3.14, True, 'world', [1, 2, 3], 'python', (4, 5), False, 7)

# In ra tuple
print(my_tuple)

# Các phần tử từ chỉ mục 2 hãy lấy 5 phần tử
print(my_tuple[2:7])

#Từ phần tử đầu đến phần tử thứ 8
print(my_tuple[:8])

# Từ phần tử thứ 4 (đếm tự nhiên) đến hết
print(my_tuple[3:])

# Từ phần tử cuối đến phần tử đầu tiên
print(my_tuple[::-1])