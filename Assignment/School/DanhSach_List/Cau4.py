# Định nghĩa danh sách
my_list = [1, 2, 3, 4, 2, 2, 3, 5, 6, 2]

# Giá trị cần đếm
value_to_count = 2

# Sử dụng phương thức count() để đếm số lần xuất hiện của giá trị cụ thể
count = my_list.count(value_to_count)

# In ra kết quả
print(f"Số lần xuất hiện của giá trị {value_to_count} trong danh sách là: {count}")
